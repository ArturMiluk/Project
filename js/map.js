function updateInfoBlock(place) {
  const title = document.querySelector(".baloon__title");
  const image = document.querySelector(".baloon__image-pic");
  const description = document.querySelector(".baloon__description");
  const address = document.querySelector(".baloon__adress");
  const imageBtn = document.querySelector(".baloon__image-btn");

  if (!title || !image || !description || !address) return;

  title.textContent = place.name;
  description.textContent = place.description;
  address.textContent = place.address;

  if (place.image) {
    image.src = place.image;
    image.style.display = "block";
  } else {
    image.style.display = "none";
  }

  if (place.url) {
    imageBtn.href = place.url;
    imageBtn.style.display = "inline-block";
  } else {
    imageBtn.href = "#";
    imageBtn.style.display = "none";
  }
}

function createAllPlacemarks(placesData, colors) {
  Object.keys(placesData).forEach((city) => {
    allPlacemarks[city] = [];

    placesData[city].forEach((place) => {
      const iconHtml = `
        <div style="
          width: 32px;
          height: 32px;
          border-radius: 50%;
          background-color: ${colors[city]};
          display: flex;
          align-items: center;
          justify-content: center;
          border: 2px solid white;
          box-shadow: 0 2px 5px rgba(0,0,0,0.3);
          cursor: pointer;
        ">
          <img src="${place.image || ""}" 
               alt="${place.name}"
               style="
                 width: 24px;
                 height: 24px;
                 border-radius: 50%;
                 object-fit: cover;
               "
               onerror="this.style.display='none'; this.parentNode.innerHTML='ðŸ›ï¸';">
        </div>
      `;

      let placemark = new ymaps.Placemark(
        place.coords,
        {
          hintContent: place.name,
        },
        {
          iconLayout: "default#imageWithContent",
          iconImageHref: "",
          iconImageSize: [40, 40],
          iconImageOffset: [-20, -20],
          iconContentLayout: ymaps.templateLayoutFactory.createClass(iconHtml),
          iconContentOffset: [0, 0],
        },
      );

      placemark.events.add("click", function () {
        updateInfoBlock(place);
      });

      placemark.events.add("mouseenter", function () {
        placemark.options.set({
          iconImageSize: [46, 46],
          iconImageOffset: [-23, -23],
        });
      });

      placemark.events.add("mouseleave", function () {
        placemark.options.set({
          iconImageSize: [40, 40],
          iconImageOffset: [-20, -20],
        });
      });

      allPlacemarks[city].push(placemark);
    });
  });
}

function handleUrlParams() {
  const urlParams = new URLSearchParams(window.location.search);
  const cityFromUrl = urlParams.get("city");

  setTimeout(() => {
    const filterSelect = document.getElementById("controlsFilters");
    if (cityFromUrl && filterSelect) {
      filterSelect.value = cityFromUrl;
      applyFilter();
    }
  }, 100);
}

function applyFilter() {
  const filterSelect = document.getElementById("controlsFilters");
  if (!filterSelect) {
    console.log("Ð¤Ð¸Ð»ÑŒÑ‚Ñ€ Ð½Ðµ Ð½Ð°Ð¹Ð´ÐµÐ½ Ð½Ð° ÑÑ‚Ñ€Ð°Ð½Ð¸Ñ†Ðµ");
    return;
  }

  const selectedCity = filterSelect.value;
  const visibleCities = [];

  map.geoObjects.removeAll();
  map.geoObjects.add(belarusPolygon);

  if (selectedCity === "belarus") {
    showAllCities();
  } else {
    if (allPlacemarks[selectedCity]) {
      allPlacemarks[selectedCity].forEach((placemark) => {
        map.geoObjects.add(placemark);
      });
      visibleCities.push(selectedCity);
    }
    updateMapCenter(visibleCities);
  }

  if (typeof window.updateActiveCity === "function") {
    window.updateActiveCity(selectedCity === "belarus" ? null : selectedCity);
  }
}

function showAllCities() {
  map.geoObjects.removeAll();
  map.geoObjects.add(belarusPolygon);

  Object.values(allPlacemarks).forEach((cityPlacemarks) => {
    cityPlacemarks.forEach((placemark) => {
      map.geoObjects.add(placemark);
    });
  });

  map.setBounds(map.geoObjects.getBounds(), {
    checkZoomRange: true,
    duration: 300,
  });
}

function updateMapCenter(visibleCities) {
  if (visibleCities.length === 0) return;

  const coordinates = [];
  visibleCities.forEach((cityId) => {
    if (allPlacemarks[cityId]) {
      allPlacemarks[cityId].forEach((placemark) => {
        coordinates.push(placemark.geometry.getCoordinates());
      });
    }
  });

  if (coordinates.length > 0) {
    map.setBounds(map.geoObjects.getBounds(), {
      checkZoomRange: true,
      duration: 300,
    });
  }
}

